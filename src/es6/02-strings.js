let hello = 'Hello';
let world = 'World';
let epicPhrase = hello + ' ' + world + '!';
console.log(epicPhrase);

// Template literals
const epicPhrase2 = `${hello} ${world}!`;
console.log(epicPhrase2);

// Multi-line strings
 const lorem = "This is a\n " + "Multi-line comment";
 const lorem2 = `This is a
  Multi-line comment
 `;

 console.log(lorem);
 console.log(lorem2);